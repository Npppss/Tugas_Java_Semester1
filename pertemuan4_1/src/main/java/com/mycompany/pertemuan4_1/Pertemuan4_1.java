public class Pertemuan4_1 {

    public static void main(String[] args) {
        // Deklarasi variabel
        int Angka1 = 10;
        int Angka2 = 3;

        // Operasi aritmatika
        int penambahan = Angka1 + Angka2;
        int pengurangan = Angka1 - Angka2;
        int perkalian = Angka1 * Angka2;
        double pembagian = (double) Angka1 / Angka2;

        // Menampilkan output
        System.out.println("a. Hasil Pertambahan A+B = " + penambahan);
        System.out.println("b. Hasil Pengurangan A-B = " + pengurangan);
        System.out.println("c. Hasil Perkalian A*B = " + perkalian);
        System.out.println("d. Hasil Pembagian A/B = " + pembagian);

    }
}
